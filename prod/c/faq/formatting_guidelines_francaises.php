<H3><A name=tables>Tableaux</A> </H3>
<P>Marquez les tables de manière à ce que le post-correcteur puisse les trouver, 
en les entourant de <TT>/*</TT> et <TT>*/</TT>, comme pour la <A 
href="#poetry">poésie</A>. 
Mettez une ligne blanche avant /* et une autre après */. Formatez les tables 
avec des espaces de manière à ce qu'elles ressemblent approximativement au 
tableau original. Ne faites pas de tableau plus large que 75 caractères. 
Les règles du projet Gutenberg ajoutent: "... sauf si vous ne pouvez pas
faire autrement. Mais JAMAIS plus de 80 caractères."</P>
<P>Pour aligner les champs, n'utilisez pas de tabulations. Seulement des espaces 
(les tabulations ont des tailles différentes suivant l'éditeur de texte). </P>
