<H3><A name=bold>Texte gras</A> </H3>
<P>Le texte gras doit être marqué par <TT>&lt;b&gt;</TT> avant et <TT>&lt;/b&gt; 
</TT>après.</P>
<P>Les signes de ponctuation doivent être HORS des marques de "gras", à moins 
que ces signes ne soient dans une phrase ou une partie de phrase entièrement en 
gras. </P>
<P>Voyez l'exemple de la section <A 
href="#page_hf">Entêtes et 
bas de page</A> . </P>
<P>Certains chefs de projet peuvent spécifier dans les commentaires de projet 
que le texte gras doit être rendu par des majuscules. </P>
<H3><A name=supers>Exposants</A> </H3>
<P>Les vieux livres abrégeaient souvent les mots en contractions, et les 
imprimaient en exposant, par exemple:<FONT color=red> <BR>Gen<SUP>rl</SUP> 
Washington defeated L<SUP>d</SUP>Cornwall's army.<BR></FONT>Insérez un chapeau 
pour identifier l' abréviation/contraction, comme suit: 
<BR>&nbsp;&nbsp;&nbsp;&nbsp; <TT>Gen^rl Washington defeated L^d Cornwall's 
army.</TT> </P>
<P>Dans les ouvrages scientifiques et techniques utilisez le "chapeau" 
<TT>^</TT> et mettez le texte en exposant entre accolades <TT>{</TT> et 
<TT>}</TT>. Mettez toujours le texte en exposant entre accolades, même si ce 
texte ne fait qu'un caractère. Ainsi: <BR>&nbsp; &nbsp; &nbsp; &nbsp; ... up to 
x<SUP>n-1</SUP> elements in the array. <BR>donne <BR>&nbsp; &nbsp; &nbsp; &nbsp; 
<TT>... up to x^{n-1} elements in the array.<BR></TT></P>
<P>Si le chef de projet dit de faire autrement dans les commentaires de projet, 
suivez ses instructions. </P>
<H3><A name=subscr>Texte en Indice</A></H3>
<P>On trouve la notation "indice" dans des ouvrages scientifiques, rarement 
ailleurs. Indiquez l'indice en mettant un signe "souligné" <TT>_</TT> devant et 
en entourant le texte en indice avec des accolades <TT>{</TT> et <TT>}</TT>. 
<BR>Par exemple: <BR>&nbsp; &nbsp; &nbsp; &nbsp; H<SUB>2</SUB>O. <BR>donne 
<BR>&nbsp; &nbsp; &nbsp; &nbsp; <TT>H_{2}O.<BR></TT></P>
<H3><A name=underl>Texte souligné</A> </H3>
<P>Marquez le texte souligné comme étant de l'<A 
href="#italics">Italique</A>, 
avec <TT>&lt;i&gt;</TT> et <TT>&lt;/i&gt;</TT>, à moins que les <A 
href="#comments">Commentaires 
de projet</A> spécifient l'utilisation de <TT>&lt;u&gt; </TT>et&nbsp; 
<TT>&lt;/u&gt; </TT>pour ce livre. Souligner était utilisé pour indiquer un 
italique quand l'éditeur était incapable d'italiciser un texte, par exemple, 
pour un document tapé à la machine. </P>
<H3><A name=espace>T e x t e&nbsp;&nbsp; e s p a c é (gesperrt)</A> </H3>
<P>Marquez ce texte en italique avec <TT>&lt;i&gt;</TT> et <TT>&lt;/i&gt; 
</TT>et enlevez les espaces en trop.&nbsp; Cette technique était utilisée pour 
mettre l'accent sur certains passages sur certains vieux livres allemands, et 
parfois italiens. Maintenant, cette fonction est remplie par les italiques, et 
les espaces supplémentaires peuvent ne pas être clairs sur certains écrans, avec
certaines fontes, quand les lecteurs liront le texte électronique. </P>
<H3><A name=#font_sz>Changement de taille de police</A> </H3>
<P>Ne faites rien pour indiquer un changement de taille de police. L'exception à
ceci est lorsque la taille de la police change pour indiquer un <A 
href="#block_qt">bloc de 
citation</A>; dans ce cas marquez le texte comme c'est spécifié pour ces cas-là.</P>

<H3><A name=word_caps>Mots entiers en majuscules</A> </H3>
<P>Si un mot ou groupe de mots dans un texte est imprimé entièrement en 
majuscules, laissez-les tels qu'ils sont dans votre 
copie de travail. </P>
<P>Une exception à cette règle est le <A 
href="#chap_head">premier 
mot d'un chapitre ou d'un paragraphe</A>: Certains livres anciens mettaient le 
premier mot de chaque paragraphe en majuscule; ce doit être changé en un mot 
normal (première lettre en majuscule, le reste en minuscule). Donc "IL était une 
fois" devient "Il était une fois". </P>
<H3><A name=small_caps>Petites capitales</A></H3>
      <P>Corrigez les mots en  <SPAN 
      style="FONT-VARIANT: small-caps">Petites capitales alternées</SPAN> en faisant 
      alterner minuscules et majuscules et entourez le texte par les marques<TT>&lt;sc&gt;</TT> et 
      <TT>&lt;/sc&gt;</TT>. Exemple: <SPAN 
      style="FONT-VARIANT: small-caps">This is Small Caps</SPAN> devient 
      <TT>&lt;sc&gt;This is Small Caps&lt;/sc&gt;</TT>. </P>
<P>Mais si un mot est imprimé <span style="font-variant: small-caps;">
tout en petites capitales </span>, alors écrivez-le en 
CAPITALES, et entourez-le de marques <tt>&lt;sc&gt;</tt>
et <tt>&lt;/sc&gt;</tt>.
   <br>
&nbsp;&nbsp;&nbsp;&nbsp;Exemple:
   You cannot be serious about
   <span style="font-variant: small-caps;">aardvarks</span>!<br>
&nbsp;&nbsp;&nbsp;&nbsp;devient:
   <tt>You cannot be serious about
   &lt;sc&gt;AARDVARKS&lt;/sc&gt;!</tt> <br>
</p>

<p>Si un mot est en capitales dans un titre (un entête de chapitre, ou de section), laissez-le
en capitales, sans marques <tt>&lt;sc&gt;</tt> et <tt>&lt;/sc&gt;</tt>. <br>
Si le premier mot d'un chapitre est en capitales, alors
changez-le en majuscules et minuscules, sans marques de petites capitales. </P>
<H3><A name=lettrine>Lettre de début de paragraphe grande ou ornée.</A> </H3>
<P>Souvent, la première lettre d'un chapitre, section ou paragraphe est imprimée 
très grande et ornée (une lettrine). Dans votre texte, laissez simplement la 
lettre. </P>
<H3><A name=em_dashes>Tirets, traits d'unions, et signe “moins”</A></H3>
<P>Vous verrez quatre types de traits dans les livres.</P>
<OL compact>
  <LI>Les tirets "<I>hyphens</I>". Ils sont utilisés pour <B>joindre</B> les
  mots, ou parfois pour joindre les préfixes ou les suffixes à un mot. Dans 
  votre texte corrigé, laissez un seul tiret, sans espace ni à droite ni à 
  gauche. 
<br>Notez l'exception à cette règle, dans le deuxième exemple
ci-dessous. </LI>
  <LI>Les tirets longs. "<I>En-dashes</I>". Ils sont un peu plus longs, ils sont 
  utilisés pour des <B>intervalles</B> de nombres, ou pour le signe mathématique 
  "moins". Là aussi, laissez un seul tiret. Laissez un espace avant ou après 
  selon la façon dont c'est imprimé sur le livre. En général, pas d'espace pour 
  les intervalles de nombres, mais, autour du signe "moins", il y en a parfois 
  des deux côtés, parfois seulement avant. </LI>
  <LI>Les tirets <I>Em-dashes &amp; long dashes. </I>Ils servent de 
  <B>séparateurs</B> entre les mots—parfois pour mettre l'accent, comme ceci—ou 
  quand une personne prend la parole, ou s'interrompt dans un dialogue. 
  Notez-les comme deux tirets. Sans espace ni avant ni après, même s'il semble y 
  en avoir un sur le document imprimé. </LI>
  <LI>Les traits qui représentent des mots (ou des noms) <B>omis</B> 
  ou <B>censurés</B>. Notez-les comme quatre tirets. Si le long trait représente 
  un mot, laissez des espaces autour des tirets, comme si c'était vraiment le 
  mot. Si c'est seulement une partie de mot, alors pas d'espaces. Joignez-le au 
  reste du mot.</LI></OL>
<P>Note. Si un tiret em-dash apparaît au début (ou à la fin) de votre ligne dans 
votre texte, joignez-le à l'autre ligne pour qu'il n'y ait pas d'espace autour 
du tiret. C'est seulement si l'auteur a utilisé un "dash" au début ou à la fin 
d'un paragraphe, ou sur une ligne de poésie, ou un dialogue que vous devez le
laissez au début ou à la fin de la ligne.</P><!-- END RR -->
