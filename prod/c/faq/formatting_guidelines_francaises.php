<H3><A name=footnotes>Notes de bas de page et de fin</A> </H3>
<P>Les notes de bas de page sont "hors ligne", autrement dit, le texte de la 
note est en bas de la page et une marque est placée dans le texte, là où elle 
est référencée. </P>
<P>Pour le formatage, ceci veut dire que : </P>
<P>1. Le numéro, la lettre, le *, ou un autre caractère qui marque la note doit 
être entouré de crochets (<TT>[</TT> et <TT>]</TT>). Enlevez les espaces avant
le <TT>[</TT>; mettez-le juste à côté du mot sur lequel porte la 
note<TT>[1]</TT> ou son signe de ponctuation,<TT>[2]</TT> comme dans le texte, 
et dans les deux exemples de cette phrase.</P>
<P>Parfois, les notes sont marquées par des séries de caractères spéciaux (*, †, 
‡, §, etc.) Dans ce cas, remplacez-les par des lettres majuscules, dans l'ordre 
(A,B,C, etc.) quand vous corrigez. </P>
<P>2.La note est entourée par la marque de note <TT>[Footnote #:&nbsp;</TT> et 
<TT>]</TT>, avec le texte de la note entre les deux, et le numéro (ou la lettre) 
de la note à la place du signe #. Laissez le texte de la note tel qu'il est 
imprimé, avec ses retours à la ligne, italiques, etc. Laissez la note en bas de 
la page. Utilisez bien la même marque de note dans la note et dans le texte (là 
où la note est référencée). </P><!-- END RR -->
<P>Pour certains livres, le chef de projet vous demandera peut-être de mettre 
les notes de bas de page en ligne. Dans ce cas, lisez les "Commentaires de 
projet". </P>
<P>Pour avoir un exemple de note de bas de page, voyez l'exemple de la section 
<A href="#page_hf">Entêtes 
et bas de page</A> . </P>
<P>Si vous voyez une note en bas d'une page, sans marque de note dans le texte, 
surtout si elle démarre au milieu d'une phrase ou d'un mot, c'est probablement 
la continuation d'une note de bas de page de la page précédente. Laissez-la au 
bas de la page, avec les autres notes de bas de page, et entourez-la par 
<TT>*[Footnote: <FONT color=red>(texte de la note)</FONT>]</TT> (sans marque ou 
numéro de note). L'étoile indique que c'est une continuation de note, et attire 
l'attention du post-correcteur. </P>
<P>Si une note continue sur la page suivante (la page s'arrête avant la note), 
laissez la note à la fin de la page, et mettez un astérisque * là où la note 
s'arrête, comme ceci <TT>[Footnote 1: <FONT 
color=red>texte-texte-texte</FONT>]*</TT>. (Le * indique que la note s'arrête
prématurément, et attire l'attention du post-correcteur qui fusionnera les deux 
parties de la note. </P>
<P>Si une note fragmentée sur plusieurs pages commence ou s'arrête sur un mot 
coupé, marquez le mot coupé <B>et</B> la note par une étoile, comme ceci. <br>
<TT>[Footnote 1: Cette note se poursuit et son dernier mot se poursuit 
aus-*]*</TT> pour le premier fragment, et <br><TT>*[Footnote: *si sur la page 
suivante.] <br></TT> pour le second fragment.</P>
<P>Si une note est référencée dans le texte mais n'apparaît pas sur cette page, 
laissez la marque de note entourée de crochets, comme d'habitude. Ce cas est 
courant dans les livres scientifiques et techniques, où les notes sont souvent 
groupées en fin de chapitre. </P>
