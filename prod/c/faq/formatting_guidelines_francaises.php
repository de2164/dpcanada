<H3><A name=sect_head>Entêtes de section</A></H3>
<P>Dans certains livres, les chapitres sont divisés en sections. Laissez les 
entêtes de section comme ils sont imprimés. Laissez deux lignes blanches avant 
cet entête, et une après (à moins que le chef de projet en ait décidé 
autrement). Si vous ne savez pas si un entête démarre un chapitre ou une
section, demandez dans la discussion de forum dédiée au projet, en précisant le 
numéro de page. </P>
<H3><A name=maj_div>Autres divisions dans les textes</A></H3>
<P>Les autres grandes divisions des textes (Préface, Avant-propos, Introduction, 
Prologue, Epilogue, Appendice, Références, Conclusion, Glossaire, Résumé, 
Remerciements, Bibliographie, etc.) seront traités comme des entêtes de 
chapitre. Quatre lignes blanches avant l'entête, et deux avant le début du 
texte. </P>
<H3><A name=para_side>Commentaires de paragraphes</A> </H3>
<P>Certains livre ont de petites descriptions des paragraphes sur le côté du 
texte. Ce sont les "Sidenotes". Déplacez ces notes juste au-dessus du paragraphe 
auquel elles appartiennent. Une Sidenote est entourée par les marques de 
Sidenote: <TT>[Sidenote:&nbsp;</TT> avant et <TT>]</TT> après. Corrigez la note 
pour qu'elle ressemble au texte imprimé, en gardant les retours à la ligne, les 
italiques, etc. Laissez une ligne blanche après la note, pour qu'elle ne se 
mélange pas avec le paragraphe durant la phase de post-correction.</P>
<P>S'il y a plusieurs notes pour un même paragraphe, mettez-les l'une après 
l'autre au début du paragraphe. Séparez-les par des lignes blanches.</P>
<P>Si le paragraphe a commencé sur une page précédente, mettez la note en haut 
de la page et marquez-la avec une astérisque ( <TT>*</TT> ) de manière à ce que 
le post-correcteur puisse voir qu'elle appartient à la page précédente. De cette 
manière: <TT>*[Sidenote: le-commentaire-description]</TT> . Le post-correcteur 
déplacera la note à l'endroit approprié.</P>
<P>Parfois, le chef de projet vous demandera de placer la note à côté de la 
phrase à laquelle elle s'applique, et pas au début ou à la fin du paragraphe. 
Dans ce cas, ne les séparez pas par des lignes blanches.</P><!-- END RR -->
