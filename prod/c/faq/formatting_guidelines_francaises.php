<H3><A name=chap_head>Entêtes de chapitres</A> </H3>
<P>Laissez les entêtes de chapitres dans le texte tels qu'ils sont imprimés.</P>
<P>Un entête de chapitre commence plus bas sur la page qu'un <A 
href="#page_hf">entête de
page</A> et n'a pas de numéro de page sur la même ligne. Les entêtes de 
chapitres sont souvent imprimées entièrement en majuscules, si c'est le cas, 
laissez-les tels quels.</P>
<P>Introduisez 4 lignes vierges avant le “CHAPITRE XXX” (insérez ces lignes 
blanches même si le chapitre démarre sur une nouvelle page; il n'y a pas de 
pages sur un livre électronique, donc les lignes blanches sont nécessaires). 
Laissez ensuite une ligne blanche entre chaque partie de l'entête du chapitre, 
comme la description du chapitre, ou une citation en ouverture, etc. et laissez 
2 lignes vierges après, entre l'entête et le texte du chapitre.</P>
<P>Les vieux livres impriment souvent le premier mot de chaque chapitre 
entièrement en majuscule; changez ces derniers en mots normaux (première lettre 
seule en majuscule).</P>
<P>Faites attention à un guillemet ( " ) au début du premier paragraphe, 
que certains éditeurs n'incluaient pas ou que les OCR ignorent à cause de la 
grande majuscule dans l'original. Si l'auteur commence le paragraphe avec un 
dialogue, insérez le guillemet. </P><!-- END RR -->
