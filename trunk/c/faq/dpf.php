<h1>The Distributed Proofreaders Foundation Canada</h1>

<p>
This document provides some basic facts about the Distributed
Proofreaders Foundation Canada (DPFC). 
</p>

<h2>Mission:</h2>

<p>
The Distributed Proofreaders Foundation Canada has been formed to support the
Distributed Proofreaders Canada (DPC) website (www.pgdpcanada.net) which has existed 
since about September, 2007. DPC volunteers
transcribe books and periodicals that are in the public domain into
electronic texts using software that has been developed by DPC
volunteers. These electronic texts are freely distributed for the
benefit of anyone who can use them. 
</p>
<p>
While currently DPC only transcribes text into ebooks, related activities
that might be included on the existing website in the future, or
supported by DPFC in some fashion, include transcription of musical
notation, cooperative translation, and public access to the scanned
material produced by DPC volunteers. In this sense, DPFC's mission goes
beyond simple support of the DPC website to the wider goal of
transcribing, into digital form, freely distributable material that is
currently only available on paper.
</p>

