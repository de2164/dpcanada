<h2>Use of Donations:</h2>

<p>
Money donated to DPFC will be used to support this website, and
other activities supportive of books in the public domain, as determined by the Board.
</p>
<p>
It is unlikely that DPC will have any paid staff in the foreseeable
future. Some software development may be contracted out, as necessary.
Funds raised by DPFC will be used to support the website, recruit new
volunteers, improve the custom software, buy additional hardware as
needed, and for such other items as the Board determines are
appropriate.
</p>

